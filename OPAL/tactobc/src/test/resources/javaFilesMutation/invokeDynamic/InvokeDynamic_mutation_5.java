public class InvokeDynamic_mutation_5 {

    public static void main(String[] args) {
        // Lambda Expression remains the same
        Runnable r = () -> System.out.println("Running in a lambda!");
        r.run();

        // Simplified String Concatenation
        String message = "Hello, World!";
        System.out.println(message);

        // Inlined Method Reference Example
        System.out.println("Method Reference Example");
    }
}
